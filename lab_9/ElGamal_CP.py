# -*- coding: utf8 -*-
"""
Цифровая подпись в стиле Эль-Гамаля (учебный вариант над Z_p^*).

Идея защиты:
- P — простое, G — основание (1 < G < P), X — секретный ключ, Y = G^X mod P — открытый.
- Одноразовый K (взаимно прост с P−1) генерируется случайно; a = G^K mod P; m = H(M).
- Второй компонент b находят из сравнения m ≡ X·a + K·b (mod P−1) (перебор в коде).
- Проверка: Y^a · a^b ≡ G^m (mod P) — при корректной подписи совпадает с G^m.

Что знать преподавателю: роль G, X, Y, K; почему K нельзя повторять; связь проверки
с дискретным логарифмом; что хеш учебный (модуль P), не SHA.
"""
import math
import secrets

# Таблица 1 (методичка): русский алфавит без Ё и Й; M_i — номер буквы (1…31).
HASH_LETTER = {
    'а': 1, 'б': 2, 'в': 3, 'г': 4, 'д': 5, 'е': 6, 'ж': 7, 'з': 8, 'и': 9, 'к': 10,
    'л': 11, 'м': 12, 'н': 13, 'о': 14, 'п': 15, 'р': 16, 'с': 17, 'т': 18, 'у': 19,
    'ф': 20, 'х': 21, 'ц': 22, 'ч': 23, 'ш': 24, 'щ': 25, 'ъ': 26, 'ы': 27, 'ь': 28,
    'э': 29, 'ю': 30, 'я': 31,
}

def is_prime(n):
    """Проверка, является ли число простым"""
    if n < 2:
        return False
    for i in range(2, int(n ** 0.5) + 1):
        if n % i == 0:
            return False
    return True

def tchk_zpt(n):
    """Замена спецсимволов на буквы"""
    n = n.replace('.', 'тчк')
    n = n.replace(',', 'зпт')
    n = n.replace(' ', 'прб')
    n = n.lower()
    n = n.replace('ё', 'е')
    n = n.replace('й', 'и')
    return n

def textToNumbers(text):
    """Номера M_i по таблице 1 (методичка)."""
    res = []
    for ch in text:
        if ch not in HASH_LETTER:
            raise ValueError(f"Символ '{ch}' отсутствует в таблице кодирования.")
        res.append(HASH_LETTER[ch])
    return res

def hesh(text, module):
    """h_i = (h_{i-1} + M_i)^2 mod module, h_0 = 0 (квадратичная свёртка)."""
    text_nums = textToNumbers(text)
    h = 0
    for m_i in text_nums:
        h = ((h + m_i) ** 2) % module
    return h

def generate_k(p):
    """Случайное K: 1 < K < p−1 и gcd(K, p−1) = 1 (нужно для поиска b)."""
    phi = p - 1
    if phi < 2:
        return None
    # Равномерно в [2, p−2]; при необходимости повторяем, пока gcd = 1
    upper = p - 2
    if upper < 2:
        return None
    for _ in range(10000):
        k = secrets.randbelow(upper - 1) + 2  # от 2 до p−2 включительно
        if math.gcd(k, phi) == 1:
            return k
    return None

def encryption(P, G, X, text):
    """Генерация цифровой подписи (название историческое: это не шифрование)."""
    if G >= P:
        print('Ошибка: G должно быть меньше P')
        return None
    if not (1 < X < P - 1):
        print('Ошибка: X вне диапазона')
        return None
    Y = pow(G, X, P)  # открытый ключ: Y = G^X mod P
    K = generate_k(P)
    if K is None:
        print('Ошибка: не удалось сгенерировать K (проверьте P).')
        return None
    print(f'Сгенерировано одноразовое K = {K} (gcd(K, P−1) = 1)')
    a = pow(G, K, P)  # первый компонент подписи: a = G^K mod P
    m = hesh(text, P)  # хеш сообщения; модуль P — согласован с проверкой G^m mod P
    print(f'Хэш: {m}')
    b = None
    # Ищем b: m ≡ X·a + K·b (mod P−1), чтобы при проверке выполнялось G^m ≡ Y^a·a^b (mod P).
    for i in range(P-1):
        if (m == (X * a + K * i) % (P-1)):
            b = i
            break
    if b is None:
        print('Ошибка: не удалось найти b')
        return None
    print(f'Открытый ключ Y = {Y}')
    return (a, b, Y, G)

def decryption(text):
    """Проверка цифровой подписи"""
    P = int(input('Введите простое число P: '))
    if not is_prime(P):
        print('Ошибка: P должно быть простым числом!')
        return
    G = int(input('Введите число G: '))
    if G >= P:
        print(f'Ошибка: G должно быть меньше P')
        return
    m = hesh(text, P)
    Y = int(input('Введите Y: '))
    S = input('Введите подпись в формате a,b: ')
    try:
        a, b = map(int, S.strip().split(','))
    except Exception:
        print('Ошибка: подпись должна быть в формате a,b')
        return
    # Проверка: Y^a · a^b mod P должно совпасть с G^m mod P (см. модульный порядок G).
    A1 = (pow(Y, a, P) * pow(a, b, P)) % P
    A2 = pow(G, m, P)
    print(f'A1 = {A1}, A2 = {A2}')
    if A1 == A2:
        print('Подпись верна')
    else:
        print('Подпись неверна')

def main():
    while True:
        print('\n--- МЕНЮ ---')
        print('1. Создать цифровую подпись')
        print('2. Проверить цифровую подпись')
        print('0. Выйти')
        choice = input('Выберите действие: ')
        if choice == '1':
            P = int(input('Введите простое число P > 32: '))
            if not is_prime(P):
                print('Ошибка: P должно быть простым числом!')
                continue
            G = int(input('Введите G (1 < G < P): '))
            if G <= 1 or G >= P:
                print('Ошибка: G должно быть в диапазоне (1, P)')
                continue
            X = int(input(f'Введите секретный ключ X (1 < X < {P - 1}): '))
            if not (1 < X < P - 1):
                print('Ошибка: X вне диапазона')
                continue
            text = input("Введите текст для подписи: ")
            text = tchk_zpt(text)
            result = encryption(P, G, X, text)
            if result:
                a, b, Y, G = result
                print(f'Подпись: ({a},{b})')
                print(f'Открытый ключ: Y = {Y}, G = {G}, P = {P}')
        elif choice == '2':
            text = input("Введите текст для проверки подписи: ")
            text = tchk_zpt(text)
            decryption(text)
        elif choice == '0':
            print('Выход из программы.')
            break
        else:
            print('Некорректный выбор. Попробуйте снова.')

if __name__ == '__main__':
    main()



