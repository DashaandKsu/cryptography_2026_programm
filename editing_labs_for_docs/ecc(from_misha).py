import ast
import random

# Алфавит русских букв (32 буквы) для преобразования чисел обратно в текст
ALPHABET = "АБВГДЕЖЗИЙКЛМНОПРСТУФХЦЧШЩЪЫЬЭЮЯ"

def replace(text):
    """Замена знаков препинания и пробелов на кодовые слова"""
    # Словарь замены символов → кодовые слова
    reps = {'.': 'ТЧК', '—': 'ТИРЕ', '–': 'ТИРЕ', ',': 'ЗПТ', '!': 'ВСКЛ', '?': 'ВПРС', '«': 'КВЧЛ', '»': 'КВЧП', ' ': 'ПРБ'}
    # Генераторное выражение: для каждого символа берём замену из словаря или сам символ
    return ''.join(reps.get(c, c) for c in text)

def restore(text):
    """Восстановление знаков препинания из кодовых слов"""
    # Последовательно заменяем каждое кодовое слово на соответствующий символ
    for code, sym in [('ТЧК','.'), ('ТИРЕ','—'), ('ЗПТ',','), ('ВСКЛ','!'), ('ВПРС','?'), ('КВЧЛ','«'), ('КВЧП','»'), ('ПРБ',' ')]:
        text = text.replace(code, sym)
    return text

def mod_inv(n, p):
    """Вычисление мультипликативного обратного числа n по модулю p (n^(-1) mod p)"""
    # Функция pow(n % p, -1, p) возвращает обратное число, если оно существует
    try: 
        return pow(n % p, -1, p)
    except: 
        return None  # Возвращаем None, если обратное не существует (n кратно p)

def is_prime(n):
    """Проверка числа n на простоту (базовый алгоритм)"""
    if n < 2: return False      # Числа меньше 2 не являются простыми
    if n == 2: return True      # 2 - простое число
    if n % 2 == 0: return False # Чётные числа больше 2 не являются простыми
    # Перебираем нечётные делители от 3 до sqrt(n)
    for i in range(3, int(n**0.5)+1, 2):
        if n % i == 0: return False
    return True

def add_points(P, Q, a, p):
    """
    Сложение двух точек на эллиптической кривой y^2 = x^3 + a*x + b по модулю p
    Возвращает точку R = P + Q или None (точка на бесконечности)
    """
    if P is None: return Q  # Если P - точка на бесконечности, возвращаем Q
    if Q is None: return P  # Если Q - точка на бесконечности, возвращаем P
    x1, y1 = P
    x2, y2 = Q
    # Если P = -Q (вертикальная прямая), сумма равна точке на бесконечности
    if x1 == x2 and (y1 + y2) % p == 0:
        return None
    # Вычисляем угловой коэффициент lambda
    if P == Q:  # Удвоение точки: lambda = (3*x1^2 + a) / (2*y1)
        num, den = (3*x1**2 + a) % p, (2*y1) % p
    else:       # Сложение разных точек: lambda = (y2 - y1) / (x2 - x1)
        num, den = (y2 - y1) % p, (x2 - x1) % p
    inv = mod_inv(den, p)  # Вычисляем обратный элемент знаменателя
    if inv is None:        # Если обратного нет - деление невозможно
        return None
    lam = (num * inv) % p  # lambda = num / den mod p
    # Вычисляем координаты результирующей точки
    x3 = (lam**2 - x1 - x2) % p
    y3 = (lam*(x1 - x3) - y1) % p
    return (x3, y3)

def scalar_mult(k, P, a, p):
    """
    Скалярное умножение точки P на число k (k*P = P + P + ... + P, k раз)
    Используется алгоритм "двой и складывай"
    """
    R = None  # Результат (начинаем с точки на бесконечности)
    Q = P     # Текущая точка (удваивается на каждом шаге)
    while k > 0:
        if k & 1:                    # Если младший бит k равен 1
            R = add_points(R, Q, a, p)  # Добавляем текущую точку к результату
        Q = add_points(Q, Q, a, p)   # Удваиваем точку
        k >>= 1                      # Сдвигаем k вправо (обрабатываем следующий бит)
    return R

def find_order(G, a, p, max_order=10000):
    """
    Нахождение порядка точки G (наименьшее n > 0 такое, что n*G = точка на бесконечности)
    Перебираем n от 1 до max_order
    """
    P = G
    for i in range(1, max_order + 1):
        if P is None:      # Если P - точка на бесконечности
            return i       # Порядок найден
        P = add_points(P, G, a, p)  # P = P + G
    return None            # Порядок не найден в заданных пределах

def encrypt_ecc(m, k, params):
    """
    Шифрование одного символа (индекса m) на эллиптической кривой
    Алгоритм Эль-Гамаля на эллиптической кривой:
    - Шифротекст: (R, e) = (k*G, m * (k*Db).x)
    - R = k*G, где G - базовая точка
    - e = m * (k*Db).x mod p, где Db - открытый ключ получателя
    """
    # Вычисляем R = k*G
    R = scalar_mult(k, params['G'], params['a'], params['p'])
    # Вычисляем точку P = k*Db (Db - открытый ключ получателя)
    P_point = scalar_mult(k, params['Db'], params['a'], params['p'])
    # Проверяем, что P_point существует и её x-координата не равна 0
    if P_point is None or P_point[0] == 0:
        return None, "Ошибка"
    # Шифротекст: (R, e = m * x_координата_P mod p)
    return (R, (m * P_point[0]) % params['p']), None

def decrypt_ecc(R, e, params):
    """
    Расшифрование одного символа (индекса) на эллиптической кривой
    - Вычисляем Q = cb*R, где cb - секретный ключ получателя
    - Так как R = k*G, то Q = cb*k*G = k*(cb*G) = k*Db
    - Восстанавливаем m = e * (x_координата_Q)^{-1} mod p
    """
    # Вычисляем Q = cb*R (cb - секретный ключ)
    Q = scalar_mult(params['cb'], R, params['a'], params['p'])
    # Проверяем, что Q существует и её x-координата не равна 0
    if Q is None or Q[0] == 0:
        return None
    # Вычисляем обратное число к x-координате Q
    inv_x = mod_inv(Q[0], params['p'])
    # Восстанавливаем исходное число m = e * x_Q^(-1) mod p
    return (e * inv_x) % params['p'] if inv_x else None

def main_ecc():
    """
    Главная функция: реализует шифрование/расшифрование на эллиптической кривой
    """
    params = {}
    
    # ========== 1. Ввод параметров эллиптической кривой ==========
    p = int(input("p (простое): "))                     # Модуль (простое число)
    a = int(input("a: "))                              # Коэффициент a в уравнении y^2 = x^3 + a*x + b
    b = int(input("b: "))                              # Коэффициент b в уравнении y^2 = x^3 + a*x + b
    gx = int(input("Gx: "))                            # x-координата базовой точки G
    gy = int(input("Gy: "))                            # y-координата базовой точки G
    G = (gx, gy)                                       # Базовая точка G
    
    # Вычисляем порядок точки G (наименьшее n, такое что n*G = точка на бесконечности)
    q = find_order(G, a, p)
    print(f"Порядок точки G: q = {q}")
    
    # Ввод секретного ключа Cb (должен быть в диапазоне 0 < Cb < q)
    cb = int(input(f"Секретный ключ Cb (0 < Cb < {q}): "))
    
    # ========== 2. Валидация введённых параметров ==========
    if not is_prime(p):                                  # Проверка, что p - простое число
        print("Ошибка: p не простое")
        return
    # Проверка, что точка G действительно лежит на кривой y^2 = x^3 + a*x + b
    if (gy**2) % p != (gx**3 + a*gx + b) % p:
        print("Ошибка: G не на кривой")
        return
    # Проверка корректности секретного ключа
    if not (0 < cb < q):
        print("Ошибка: недопустимый ключ")
        return

    # Сохраняем параметры в словарь
    params.update({'p': p, 'a': a, 'b': b, 'G': (gx, gy), 'q': q})

    # ========== 3-4. Выбор режима работы ==========
    mode = input("\nВыберите режим 1. Зашифровать 2. Расшифровать ")
    
    if mode == '1':  # ========== РЕЖИМ ШИФРОВАНИЯ ==========
        text = input("\nВведите текст: ").upper()
        
        # Вычисляем открытый ключ Db = cb * G
        Db = scalar_mult(cb, params['G'], params['a'], params['p'])
        params.update({'cb': cb, 'Db': Db})
        
        # Шифруем текст посимвольно
        cipher = []  # Список для хранения зашифрованных данных
        k_values = []  # Список сгенерированных значений k (для вывода)
        for ch in replace(text.upper()):        # Заменяем знаки препинания на кодовые слова
            if ch in ALPHABET:                  # Если символ - русская буква
                idx = ALPHABET.index(ch) + 1    # Получаем индекс буквы (1-32, чтобы не было m=0)
                # Пробуем случайные k пока не получим успешное шифрование
                for _ in range(100):
                    k = random.randint(2, params['q'] - 1)  # Генерируем случайное k
                    res, _ = encrypt_ecc(idx, k, params)    # Шифруем индекс
                    if res is not None:
                        cipher.append(res)          # Добавляем результат в список
                        k_values.append(k)          # Сохраняем k
                        break

        print(f"\nОткрытый ключ Db: {Db}")
        # Выводим первые 10 значений k из общего количества
        print(f"\nПервые 10 из {len(k_values)} случайных k: {k_values[:10]}")
        print(f"\nЗашифрованное сообщение: {cipher}")
        
    elif mode == '2':  # ========== РЕЖИМ РАСШИФРОВАНИЯ ==========
        params['cb'] = cb
        
        # Ввод зашифрованных данных
        raw = input("Введите зашифрованные данные: ")
        data = ast.literal_eval(raw)  # Безопасное преобразование строки в объект Python
        
        # Расшифровываем каждый элемент списка
        result = ''.join(
            ALPHABET[decrypt_ecc(item[0], item[1], params) - 1]   # Преобразуем число в букву (индекс со сдвигом -1)
            for item in data
            if decrypt_ecc(item[0], item[1], params) is not None  # Только успешно расшифрованные
        )
        # Восстанавливаем знаки препинания и выводим результат в нижнем регистре
        print(f"\nРасшифрованный текст: {restore(result).lower()}")


if __name__ == "__main__":
    main_ecc()